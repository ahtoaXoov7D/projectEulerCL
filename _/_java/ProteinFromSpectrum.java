  public static String specToProt( double[] spec ) {
    HashMap<Integer,Character> massToAA = createAAtable();
    String out = "";
    for(int i = 1; i < spec.length; ++i) {
      int val = (int)((spec[i]-spec[i-1])*10000+0.5);
      out += massToAA.get(val);
    }
    return out;
  }
  
